def write_methodology(f):
    """Condensed methodology ported/adapted for LaTeX."""
    f.write(r"""
\chapter{Methodology and Data Sources}

\section{RTLP Score (R)}
R is the simple average of nine binary indicators (0 = no protection, 1 = full protection). Eight indicators are derived from V-Dem v15 (2024 data) using the following thresholds (higher = stronger protection):

\begin{enumerate}
  \item Legal Protections — \texttt{v2cltrnslw} $\ge 2.0$
  \item Independent Judiciary — average(\texttt{v2juhcind}, \texttt{v2juncind}) $\ge 2.0$
  \item Law Enforcement Accountability — \texttt{v2clkill} $\ge 2.0$
  \item Protection Against Arbitrary Detention — \texttt{v2xcl\_acjst} $\ge 0.5$
  \item Freedom from Torture — \texttt{v2cltort} $\ge 2.0$
  \item Civilian Protection in Conflict — \texttt{v2clkill} $\ge 2.0$
  \item Access to Justice — \texttt{v2xcl\_acjst} $\ge 0.5$
  \item Freedom of Expression \& Whistleblower Protections — \texttt{v2x\_freexp} $\ge 0.5$
\end{enumerate}

9. Socioeconomic Conditions — World Bank: undernourishment $\le 5\%$ AND poverty headcount (\$2.15) $\le 10\%$.

R = (number of `Yes' indicators) / 9

\section{Economic Projection}
$\Delta G$ (per-capita loss) = $0.05 \times (1 - R) \times G_0$, where $G_0$ is the most recent published GDP per capita (World Bank, labeled 2026 baseline in this edition). Total national loss = $\Delta G \times$ population.

\section{Data Vintage Note for 2026 Edition}
V-Dem components reflect the latest available year in the source file (2024). GDP per capita ($G_0$) uses the freshest published values available at the time of atlas production. This follows the principle that RTLP governance scores are relatively stable year-to-year while GDP is the more dynamic variable in the equation.

Full crosswalk and binarization rules: \texttt{docs/indicator\_crosswalk.md}

Source equations: Sid J.A. Hubbard, \textit{Causality and Attraction} v3 (2026), DOI 10.5281/zenodo.19468550.
""")

def write_global_summary_table(f, atlas_df):
    """The big 193-row summary table using longtable + booktabs for refined design."""
    f.write(r"""
\chapter{Summary Table of All 193 UN Member Nations}

Sorted by estimated total annual GDP loss (highest first). R = RTLP score (0–1); values shown as fraction of 9 indicators where possible. Losses in current USD using 2026 baseline GDP per capita paired with latest available RTLP components (2024).

\begin{longtable}{@{}r l l c r r r@{}}
\toprule
\textbf{\#} & \textbf{Country} & \textbf{Region} & \textbf{R} & \textbf{Per Capita Loss} & \textbf{Total Loss (USD)} & \textbf{Population} \\
\midrule
\endfirsthead
\toprule
\textbf{\#} & \textbf{Country} & \textbf{Region} & \textbf{R} & \textbf{Per Capita Loss} & \textbf{Total Loss (USD)} & \textbf{Population} \\
\midrule
\endhead
\bottomrule
\endfoot
""")

    # Sort by total lost (as in original)
    sorted_df = atlas_df.sort_values("total_deficit_usd", ascending=False).reset_index(drop=True)

    for idx, row in sorted_df.iterrows():
        rank = idx + 1
        country = str(row["country"])[:26].replace("&", "\\&")
        region = str(row.get("UNregion", ""))[:16].replace("&", "\\&")
        r = row["r"]
        r_str = f"{r:.2f}" if pd.notna(r) else "N/A"
        per_cap = row["delta_g_per_capita"]
        per_cap_str = f"${per_cap:,.0f}" if pd.notna(per_cap) else "N/A"
        total = row["total_deficit_usd"]
        if pd.notna(total):
            if total >= 1e9:
                total_str = f"${total/1e9:.1f} bn"
            else:
                total_str = f"${total/1e6:.0f} m"
        else:
            total_str = "N/A"
        pop = row["population"]
        pop_str = f"{pop/1e6:.1f} m" if pd.notna(pop) else "N/A"

        f.write(f"{rank} & {country} & {region} & {r_str} & {per_cap_str} & {total_str} & {pop_str} \\\\\n")

    f.write(r"""
\bottomrule
\end{longtable}
""")

