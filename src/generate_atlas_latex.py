def load_data():
    """Load the same 2026 data used by the fpdf generator."""
    atlas = pd.read_csv(ATLAS_CSV)
    with open(BREAKDOWN_JSON) as f:
        breakdown = json.load(f)
    # Make lookup by iso3 for convenience
    breakdown_by_iso = {d["iso3"]: d for d in breakdown}
    return atlas, breakdown, breakdown_by_iso

